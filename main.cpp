#include <iostream>
using namespace std;

int main(int argc, const char * argv[]) {

    int a, b;
    
    cout << "Введите первое число: ";
    cin >> a;
    
    cout << "Введите второе число: ";
    cin >> b;
    
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;

    return 0;
}
